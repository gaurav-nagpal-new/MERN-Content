public class Swap {
    public static void main(String[] args) {
        int n1 = 10;
        int n2 = 20;

        int temp = n1; // temp = 10
        n1 = n2; // n1 = 20
        n2 = temp; // n2 = 10

        System.out.println("n1 = " + n1);
        System.out.println("n2 = " + n2);
    }
}
